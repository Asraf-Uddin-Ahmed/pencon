    /*updating new problem*/
    void updateProblem(){
        ProblemSet problemSet = new ProblemSet();
        
        //updating problem
        if( problemSet.addUpdateProblem((char)(jComboBoxSelectProblem.getSelectedIndex()-1+'A'), jTextFieldName.getText(), 
                jTextFieldInputFile.getText(), jTextFieldOutputFile.getText(), jTextFieldTimeLimit.getText(), 
                jTextFieldMemoryLimit.getText())==true ){
            JOptionPane.showMessageDialog(rootPane, "Problem is updated.", "Successful", JOptionPane.INFORMATION_MESSAGE);
            dispose();
        }
        else{
            JOptionPane.showMessageDialog(rootPane, "You did not give correct information.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }
    
