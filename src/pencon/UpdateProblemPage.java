private void jComboBoxSelectProblemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComboBoxSelectProblemActionPerformed
    int selectedIndex = jComboBoxSelectProblem.getSelectedIndex();
    if( selectedIndex==0 ){
        jTextFieldName.setText(null);
        jTextFieldInputFile.setText(null);
        jTextFieldOutputFile.setText(null);
        jTextFieldTimeLimit.setText(null);
        jTextFieldMemoryLimit.setText(null);
        return ;
    }
    
    ProblemSet problemSet = new ProblemSet();
    char problemId = (char) (selectedIndex-1+'A');
    jTextFieldName.setText(problemSet.getProblemName(problemId));
    jTextFieldTimeLimit.setText(problemSet.getProblemTimeLimit(problemId));
    jTextFieldMemoryLimit.setText(problemSet.getProblemMemoryLimit(problemId));
    jTextFieldInputFile.setText(problemSet.isInputFileExists(problemId));
    jTextFieldOutputFile.setText(problemSet.isOutputFileExists(problemId));
    
}//GEN-LAST:event_jComboBoxSelectProblemActionPerformed

